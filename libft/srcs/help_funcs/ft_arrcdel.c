#include "libft.h"

void	ft_arrcdel(char **arr)
{
	int i;

	i = -1;
	while (arr && arr[++i])
	{
		free(arr[i]);
		arr[i] = NULL;
	}
	if (arr)
	{
		free(arr);
		arr = NULL;
	}
}
