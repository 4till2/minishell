#include "libft.h"

unsigned int	ft_nbrsize(int n)
{
	unsigned int size;

	size = 1;
	n *= (n < 0 ? -1 : 1);
	while (n >= 10)
	{
		n /= 10;
		++size;
	}
	return (size);
}
