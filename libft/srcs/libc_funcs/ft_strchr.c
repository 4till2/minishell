#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	char	ch;

	ch = (char)c;
	while (*s)
	{
		if (*s == ch)
		{
			return ((char*)s);
		}
		++s;
	}
	if (ch == '\0')
		return ((char*)s);
	return (0);
}
